/*

JavaScript has various operators that allow you to perform operations on variables and values. Here's a brief overview of some of the key operators in JavaScript:

Comparison Operators:
Equal (==): Checks if two values are equal.
Not Equal (!=): Checks if two values are not equal.
Strict Equal (===): Checks if two values are equal and of the same type.
Strict Not Equal (!==): Checks if two values are not equal or not of the same type.
Greater Than (>): Checks if the left operand is greater than the right operand.
Less Than (<): Checks if the left operand is less than the right operand.
Greater Than or Equal To (>=): Checks if the left operand is greater than or equal to the right operand.
Less Than or Equal To (<=): Checks if the left operand is less than or equal to the right operand.

*/

